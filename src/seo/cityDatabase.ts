const slugIndex = new Map<string, CityData>(
  allCities.map((city) => [city.slug, city]),
);

export function getCityBySlug(slug: string): CityData | undefined {
  return slugIndex.get(slug);
}

export function getCitiesByCountry(countryCode: string): CityData[] {
  return allCities.filter((city) => city.countryCode === countryCode);
}

const DACH_CODES = new Set(['DE', 'AT', 'CH']);

export function getDACHCities(): CityData[] {
  return allCities.filter((city) => DACH_CODES.has(city.countryCode));
}

export function getInternationalCities(): CityData[] {
  return allCities.filter((city) => !DACH_CODES.has(city.countryCode));
}
